"""
Clinical subgroup fairness and model disparity evaluation module.

Evaluates diagnostic performance across patient subgroups (gender, age groups,
and ethnicities) to ensure clinical equity (Experiment 6).
"""

from __future__ import annotations

import os
import matplotlib.pyplot as plt
import numpy as np
import pandas as pd
import seaborn as sns
from loguru import logger
from sklearn.metrics import roc_auc_score, confusion_matrix


def evaluate_subgroup_fairness(
    df: pd.DataFrame,
    y_true: pd.Series,
    y_prob: pd.Series,
    subgroup_cols: list[str] | None = None,
    threshold: float = 0.5,
) -> pd.DataFrame:
    """Compute performance metrics individually across demographic subgroups.

    Args:
        df: DataFrame containing the subgroup indicator columns.
        y_true: True binary labels.
        y_prob: Predicted risk probabilities.
        subgroup_cols: List of column names (e.g., ['gender', 'age_group']).
        threshold: Urgency cutoff.

    Returns:
        DataFrame summarizing metrics for all subgroups.
    """
    if subgroup_cols is None:
        subgroup_cols = ["gender", "age_group"]
        
    df = df.copy()
    df["label"] = y_true.values
    df["prob"] = y_prob.values
    df["pred"] = (df["prob"] >= threshold).astype(int)
    
    # Define age groups if not present
    if "age_group" not in df.columns and "age" in df.columns:
        df["age_group"] = pd.cut(
            df["age"],
            bins=[0, 45, 60, 75, 120],
            labels=["<45", "45-59", "60-74", "75+"]
        )
        
    records = []
    
    for col in subgroup_cols:
        if col not in df.columns:
            logger.warning("Subgroup column '{}' not found in data. Skipping.", col)
            continue
            
        for group_name, group_df in df.groupby(col):
            # Skip subgroups with very small counts
            if len(group_df) < 5:
                continue
                
            y_g_true = group_df["label"].to_numpy()
            y_g_prob = group_df["prob"].to_numpy()
            y_g_pred = group_df["pred"].to_numpy()
            
            # Subgroup metrics
            n_samples = len(group_df)
            selection_rate = float(np.mean(y_g_pred))
            
            if len(np.unique(y_g_true)) < 2:
                auroc = 0.5
            else:
                auroc = float(roc_auc_score(y_g_true, y_g_prob))
                
            tn, fp, fn, tp = confusion_matrix(
                y_g_true, y_g_pred, labels=[0, 1]
            ).ravel()
            
            sensitivity = float(tp / (tp + fn)) if (tp + fn) > 0 else 0.0
            specificity = float(tn / (tn + fp)) if (tn + fp) > 0 else 0.0
            
            records.append({
                "subgroup_attribute": col,
                "subgroup_value": str(group_name),
                "sample_size": n_samples,
                "auroc": auroc,
                "sensitivity": sensitivity,
                "specificity": specificity,
                "selection_rate": selection_rate,
            })
            
    return pd.DataFrame(records)


def assess_disparities(subgroup_df: pd.DataFrame, metric: str = "auroc") -> dict:
    """Calculate disparity ratio and differences for a performance metric.

    For a metric, Disparity Ratio = min_value / max_value.
    A ratio close to 1.0 indicates equity. Values below 0.8 trigger concern (80% rule).
    """
    if subgroup_df.empty:
        return {"disparity_ratio": 1.0, "disparity_difference": 0.0}
        
    # Group by attribute and compute min/max
    summary = {}
    
    for attr, group in subgroup_df.groupby("subgroup_attribute"):
        vals = group[metric].to_numpy()
        if len(vals) < 2:
            continue
            
        v_min = np.min(vals)
        v_max = np.max(vals)
        
        ratio = v_min / max(v_max, 1e-6)
        diff = v_max - v_min
        
        summary[attr] = {
            "disparity_ratio": float(ratio),
            "disparity_difference": float(diff),
            "underperforming_group": str(group.loc[group[metric].idxmin(), "subgroup_value"]),
            "overperforming_group": str(group.loc[group[metric].idxmax(), "subgroup_value"]),
        }
        
        logger.info(
            "Fairness Check (Attribute={}): Disparity Ratio for {} = {:.3f} (min={} vs max={})",
            attr,
            metric,
            ratio,
            group.loc[group[metric].idxmin(), "subgroup_value"],
            group.loc[group[metric].idxmax(), "subgroup_value"]
        )
        
    return summary


def plot_fairness_disparities(subgroup_df: pd.DataFrame, output_path: str) -> None:
    """Plot subgroup AUROCs and print disparity metrics.

    Args:
        subgroup_df: DataFrame generated by evaluate_subgroup_fairness.
        output_path: Path to save output plot.
    """
    if subgroup_df.empty:
        return
        
    plt.figure(figsize=(10, 6))
    
    # Combined label for plotting
    subgroup_df = subgroup_df.copy()
    subgroup_df["Display Label"] = (
        subgroup_df["subgroup_attribute"] + ": " + subgroup_df["subgroup_value"]
    )
    
    sns.barplot(
        data=subgroup_df,
        y="Display Label",
        x="auroc",
        hue="subgroup_attribute",
        dodge=False,
        palette="Set3"
    )
    
    plt.axvline(x=0.8, color="red", linestyle="--", alpha=0.7, label="Fairness Target (0.80)")
    plt.axvline(x=0.5, color="gray", linestyle=":", alpha=0.5)
    plt.title("Model AUROC Across Demographic Subgroups")
    plt.xlabel("AUROC Score")
    plt.ylabel("Demographic Subgroup")
    plt.xlim(0.4, 1.0)
    plt.legend(title="Subgroup Type", loc="lower left")
    plt.tight_layout()
    
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    plt.savefig(output_path, dpi=300)
    plt.close()
    logger.info("Fairness subgroup plot saved to {}", output_path)
